#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, extname, relative, resolve, sep } from "node:path";
import { pathToFileURL } from "node:url";
import { collectSelfHostEnvVars } from "../../../scripts/gen-selfhost-env-reference.js";

export const DEFAULT_OUTPUT_PATH = "packages/loopover-miner/docs/env-reference.md";
export const DEFAULT_MODULE_OUTPUT_PATH = "apps/loopover-ui/src/lib/ams-env-reference.ts";
export const DEFAULT_SOURCE_ROOTS = [
  "packages/loopover-miner/bin",
  "packages/loopover-miner/lib",
  "packages/loopover-miner/scripts",
  // The coding-agent driver (MINER_CODING_AGENT_* vars) lives in the shared engine package, not under
  // packages/loopover-miner itself -- scoped narrowly to src/miner so this doesn't pull in unrelated
  // packages/loopover-engine env vars that belong to a different generator/doc. (#6994)
  "packages/loopover-engine/src/miner",
];

const MINER_ENV_PREFIXES = ["LOOPOVER_MINER_", "MINER_"];
const SOURCE_FILE_EXTENSIONS = new Set([".cjs", ".js", ".mjs", ".ts", ".tsx"]);
const DEFAULT_PATTERNS = [
  /env\.([A-Z][A-Z0-9_]*)\s*\?\?\s*""/g,
  /env\.([A-Z][A-Z0-9_]*)\s*\?\?\s*''/g,
  /env\.([A-Z][A-Z0-9_]*)\s*\?\?\s*"([^"]*)"/g,
  /env\.([A-Z][A-Z0-9_]*)\s*\?\?\s*'([^']*)'/g,
  /env\.([A-Z][A-Z0-9_]*)\.trim\(\)\s*:\s*""/g,
  /env\.([A-Z][A-Z0-9_]*)\.trim\(\)\s*:\s*''/g,
  /\{\s*([A-Z][A-Z0-9_]*)\s*=\s*"([^"]*)"\s*\}\s*=\s*process\.env/g,
  /\{\s*([A-Z][A-Z0-9_]*)\s*=\s*'([^']*)'\s*\}\s*=\s*process\.env/g,
];

export function isMinerEnvVar(name) {
  return MINER_ENV_PREFIXES.some((prefix) => name.startsWith(prefix));
}

export function collectMinerEnvDefaults(rootDir, sourceRoots = DEFAULT_SOURCE_ROOTS) {
  const defaults = new Map();
  for (const file of sourceFiles(rootDir, sourceRoots)) {
    const abs = resolve(rootDir, file);
    const source = readFileSync(abs, "utf8");
    for (const pattern of DEFAULT_PATTERNS) {
      pattern.lastIndex = 0;
      for (const match of source.matchAll(pattern)) {
        const name = match[1];
        if (!isMinerEnvVar(name)) continue;
        const value = match[2] ?? "";
        if (!defaults.has(name)) defaults.set(name, value);
      }
    }
  }
  return defaults;
}

export function collectMinerEnvVars({
  rootDir = process.cwd(),
  sourceRoots = DEFAULT_SOURCE_ROOTS,
} = {}) {
  const defaults = collectMinerEnvDefaults(rootDir, sourceRoots);
  const rows = collectSelfHostEnvVars({ rootDir, sourceRoots })
    .filter((row) => isMinerEnvVar(row.name))
    .map((row) => ({
      name: row.name,
      firstReference: row.firstReference.replace(/^packages\/loopover-miner\//, ""),
      defaultValue: defaults.has(row.name) ? defaults.get(row.name) : null,
    }));
  return rows;
}

export function renderMinerEnvReferenceMarkdown(rows) {
  const header = [
    "# loopover-miner environment variable reference",
    "",
    "Generated by `npm run miner:env-reference`. Do not edit manually.",
    "",
    "| Name | First reference | Default |",
    "| --- | --- | --- |",
  ];
  const body = rows.map((row) => {
    const defaultCell =
      row.defaultValue === null || row.defaultValue === undefined
        ? "(none)"
        : row.defaultValue === ""
          ? "`\"\"`"
          : `\`${JSON.stringify(row.defaultValue)}\``;
    return `| \`${row.name}\` | \`${row.firstReference}\` | ${defaultCell} |`;
  });
  return [...header, ...body, ""].join("\n");
}

export function writeMinerEnvReference({
  rootDir = process.cwd(),
  outputPath = DEFAULT_OUTPUT_PATH,
  sourceRoots = DEFAULT_SOURCE_ROOTS,
  check = false,
} = {}) {
  const rows = collectMinerEnvVars({ rootDir, sourceRoots });
  const output = renderMinerEnvReferenceMarkdown(rows);
  const absOutput = resolve(rootDir, outputPath);
  const current = existsSync(absOutput) ? readFileSync(absOutput, "utf8") : null;
  const changed = current !== output;
  if (!check && changed) {
    mkdirSync(dirname(absOutput), { recursive: true });
    writeFileSync(absOutput, output);
  }
  return { changed, outputPath, rows };
}

// Mirrors scripts/gen-selfhost-env-reference.ts's renderSelfHostEnvReferenceModule -- same shape
// (a typed rows array plus a pre-joined markdown string), so docs.ams-env-reference.tsx's content
// can import AMS_ENV_REFERENCE_MARKDOWN and render it exactly the way
// docs.self-hosting-configuration.tsx already renders SELFHOST_ENV_REFERENCE_MARKDOWN. The
// row-level default value is AMS-specific (ORB's self-host reference has no equivalent column).
// Project Prettier config defaults to double-quoted JS string literals (singleQuote: false), but
// still switches a given string to single quotes when that needs fewer backslash-escapes -- which
// every markdown line whose row has a quoted string defaultValue (rendered as `""`/`"production"`
// in the table) hits, since JSON.stringify always double-quotes. Replicate that per-string choice
// here so the generator's own output is already Prettier-clean, not dependent on a separate --fix.
function quoteJsStringLiteral(value) {
  const doubleQuoteCount = (value.match(/"/g) ?? []).length;
  const singleQuoteCount = (value.match(/'/g) ?? []).length;
  if (singleQuoteCount < doubleQuoteCount) {
    const escaped = value.replace(/\\/g, "\\\\").replace(/'/g, "\\'").replace(/\n/g, "\\n");
    return `'${escaped}'`;
  }
  return JSON.stringify(value);
}

export function renderMinerEnvReferenceModule(rows) {
  const markdown = renderMinerEnvReferenceMarkdown(rows);
  const rowLines = rows
    .map(
      (row) =>
        `  {\n    name: ${quoteJsStringLiteral(row.name)},\n    firstReference: ${quoteJsStringLiteral(row.firstReference)},\n    defaultValue: ${row.defaultValue === null ? "null" : quoteJsStringLiteral(row.defaultValue)},\n  },`,
    )
    .join("\n");
  const markdownLines = markdown
    .split("\n")
    .map((line) => `  ${quoteJsStringLiteral(line)},`)
    .join("\n");
  return `// Generated by scripts/generate-env-reference.mjs (npm run miner:env-reference). Do not edit manually.
export type MinerEnvReferenceRow = {
  name: string;
  firstReference: string;
  defaultValue: string | null;
};

export const AMS_ENV_REFERENCE_ROWS: MinerEnvReferenceRow[] = [
${rowLines}
];

export const AMS_ENV_REFERENCE_MARKDOWN = [
${markdownLines}
].join("\\n");
`;
}

export function writeMinerEnvReferenceModule({
  rootDir = process.cwd(),
  outputPath = DEFAULT_MODULE_OUTPUT_PATH,
  sourceRoots = DEFAULT_SOURCE_ROOTS,
  check = false,
} = {}) {
  const rows = collectMinerEnvVars({ rootDir, sourceRoots });
  const output = renderMinerEnvReferenceModule(rows);
  const absOutput = resolve(rootDir, outputPath);
  const current = existsSync(absOutput) ? readFileSync(absOutput, "utf8") : null;
  const changed = current !== output;
  if (!check && changed) {
    mkdirSync(dirname(absOutput), { recursive: true });
    writeFileSync(absOutput, output);
  }
  return { changed, outputPath, rows };
}

function sourceFiles(rootDir, sourceRoots) {
  const files = [];
  for (const sourceRoot of sourceRoots) {
    const abs = resolve(rootDir, sourceRoot);
    if (!existsSync(abs)) continue;
    const stat = statSync(abs);
    if (stat.isFile()) {
      if (!isSupportedSourceFile(abs)) throw new Error(`Unsupported source root file extension: ${sourceRoot}`);
      files.push(toPosixPath(relative(rootDir, abs)));
      continue;
    }
    if (!stat.isDirectory()) throw new Error(`Unsupported source root: ${sourceRoot}`);
    for (const file of walkSourceFiles(abs)) {
      files.push(toPosixPath(relative(rootDir, file)));
    }
  }
  return files;
}

const COMPILED_JS_EXTENSIONS = new Set([".js", ".mjs", ".cjs"]);

function walkSourceFiles(dir) {
  const files = [];
  const entries = readdirSync(dir, { withFileTypes: true }).sort((a, b) => a.name.localeCompare(b.name));
  // Same reasoning as gen-selfhost-env-reference.ts's identical guard: a same-basename .ts/.tsx sibling
  // means the .js is that source's compiled output (gitignored, built on demand since #7290/#7291/#7705),
  // so whether it exists on disk varies by environment -- skip it so the collected DEFAULT_PATTERNS
  // defaults don't silently depend on whether a build happened to run first.
  const tsBasenames = new Set(
    entries.filter((entry) => entry.isFile() && (entry.name.endsWith(".ts") || entry.name.endsWith(".tsx"))).map((entry) => entry.name.slice(0, entry.name.lastIndexOf("."))),
  );
  for (const entry of entries) {
    const abs = resolve(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkSourceFiles(abs));
    } else if (entry.isFile() && isSupportedSourceFile(abs) && !entry.name.endsWith(".d.ts")) {
      const ext = extname(entry.name);
      if (COMPILED_JS_EXTENSIONS.has(ext) && tsBasenames.has(entry.name.slice(0, -ext.length))) continue;
      files.push(abs);
    }
  }
  return files;
}

function isSupportedSourceFile(file) {
  return SOURCE_FILE_EXTENSIONS.has(extname(file));
}

function toPosixPath(path) {
  return path.split(sep).join("/");
}

function main(argv) {
  const check = argv.includes("--check");
  const results = [writeMinerEnvReference({ check }), writeMinerEnvReferenceModule({ check })];
  const stale = results.filter((result) => result.changed);
  if (check && stale.length > 0) {
    for (const result of stale) {
      process.stderr.write(
        `generate-env-reference: ${result.outputPath} is stale; run npm run miner:env-reference.\n`,
      );
    }
    process.exit(1);
  }
  for (const result of results) {
    process.stdout.write(
      `generate-env-reference: ${check ? "checked" : "wrote"} ${result.rows.length} env var references in ${result.outputPath}\n`,
    );
  }
}

if (import.meta.url === pathToFileURL(process.argv[1] ?? "").href) {
  main(process.argv.slice(2));
}
